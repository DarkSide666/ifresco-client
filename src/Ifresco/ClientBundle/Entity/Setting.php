<?php

namespace Ifresco\ClientBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Setting
 */
class Setting
{
    /**
     * @var integer
     */
    private $id;

    /**
     * @var string
     */
    private $key_string;

    /**
     * @var string
     */
    private $value_string;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set key_string
     *
     * @param string $keyString
     * @return Setting
     */
    public function setKeyString($keyString)
    {
        $this->key_string = $keyString;
    
        return $this;
    }

    /**
     * Get key_string
     *
     * @return string 
     */
    public function getKeyString()
    {
        return $this->key_string;
    }

    /**
     * Set value_string
     *
     * @param string $valueString
     * @return Setting
     */
    public function setValueString($valueString)
    {
        $this->value_string = $valueString;
    
        return $this;
    }

    /**
     * Get value_string
     *
     * @return string 
     */
    public function getValueString()
    {
        return $this->value_string;
    }
}
